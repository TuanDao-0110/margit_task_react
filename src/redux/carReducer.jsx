import { ADD_ITEM, DELETE_ITEM } from "./action";

const initialState = {
  carNumber: 20,
};

const carReducer = (state = initialState, action) => {
  switch (action.type) {
    case ADD_ITEM:
      return {
        ...state,
        numOfItems: state.numOfItems + 1,
      };

    case DELETE_ITEM:
      return {
        ...state,
        numOfItems: state.numOfItems - 1,
      };
    default:
      return state;
  }
};
export default carReducer;
